from __future__ import annotations

from itertools import product
import json
from pathlib import Path
import unittest


ROOT = Path(__file__).resolve().parents[1]
DATASETS = [
    ROOT / "datasets" / "seed_probes_v0" / "items.jsonl",
    ROOT / "datasets" / "template_probes_v0" / "items.jsonl",
]
MAX_COMBINATIONS = 512


class DatasetItemConsistencyTests(unittest.TestCase):
    def test_text_with_blanks_matches_segments_and_blank_ids(self) -> None:
        for dataset_path in DATASETS:
            for item in _load_items(dataset_path):
                with self.subTest(dataset=dataset_path.parent.name, item_id=item["item_id"]):
                    rendered = _render_with_blank_markers(item)
                    self.assertEqual(item["text_with_blanks"], rendered)

    def test_expected_full_texts_are_reachable_from_accepted_fills(self) -> None:
        for dataset_path in DATASETS:
            for item in _load_items(dataset_path):
                with self.subTest(dataset=dataset_path.parent.name, item_id=item["item_id"]):
                    expected = set(item["expected_full_texts"])
                    accepted_outputs = _render_fill_combinations(item, fill_bucket="accepted_fills")
                    self.assertTrue(expected, "expected_full_texts must not be empty")
                    self.assertTrue(
                        expected.issubset(accepted_outputs),
                        f"expected_full_texts must be generated by accepted_fills: missing={expected - accepted_outputs}",
                    )

    def test_known_wrong_fills_do_not_generate_expected_full_texts(self) -> None:
        for dataset_path in DATASETS:
            for item in _load_items(dataset_path):
                expected = set(item["expected_full_texts"])
                for blank in item["blanks"]:
                    for wrong_fill in blank.get("known_wrong_fills", []):
                        with self.subTest(dataset=dataset_path.parent.name, item_id=item["item_id"], blank_id=blank["blank_id"], wrong_fill=wrong_fill):
                            outputs = _render_with_one_forced_fill(item, blank["blank_id"], wrong_fill)
                            self.assertFalse(expected.intersection(outputs))

    def test_expected_error_pattern_fills_are_known_wrong(self) -> None:
        for dataset_path in DATASETS:
            for item in _load_items(dataset_path):
                for blank in item["blanks"]:
                    known_wrong = set(blank.get("known_wrong_fills", []))
                    for pattern in blank.get("expected_error_patterns", []):
                        with self.subTest(dataset=dataset_path.parent.name, item_id=item["item_id"], blank_id=blank["blank_id"]):
                            self.assertIn(pattern["fill"], known_wrong)


def _load_items(path: Path) -> list[dict]:
    with path.open("r", encoding="utf-8") as handle:
        return [json.loads(line) for line in handle if line.strip()]


def _render_with_blank_markers(item: dict) -> str:
    segments = item["segments"]
    blanks = item["blanks"]
    parts: list[str] = []
    for index, segment in enumerate(segments):
        parts.append(segment)
        if index < len(blanks):
            parts.append("（" + blanks[index]["blank_id"] + "）")
    return "".join(parts)


def _render_fill_combinations(item: dict, *, fill_bucket: str) -> set[str]:
    fill_options = [blank.get(fill_bucket, []) for blank in item["blanks"]]
    _assert_reasonable_combination_count(item, fill_options)
    return {_render_with_fills(item, fills) for fills in product(*fill_options)}


def _render_with_one_forced_fill(item: dict, blank_id: str, forced_fill: str) -> set[str]:
    fill_options = []
    for blank in item["blanks"]:
        if blank["blank_id"] == blank_id:
            fill_options.append([forced_fill])
        else:
            fill_options.append(blank["accepted_fills"])
    _assert_reasonable_combination_count(item, fill_options)
    return {_render_with_fills(item, fills) for fills in product(*fill_options)}


def _render_with_fills(item: dict, fills: tuple[str, ...]) -> str:
    segments = item["segments"]
    parts: list[str] = []
    for index, segment in enumerate(segments):
        parts.append(segment)
        if index < len(fills):
            parts.append(fills[index])
    return "".join(parts)


def _assert_reasonable_combination_count(item: dict, fill_options: list[list[str]]) -> None:
    count = 1
    for values in fill_options:
        count *= len(values)
    if count > MAX_COMBINATIONS:
        raise AssertionError(f"too many fill combinations for static consistency test: item_id={item['item_id']} count={count}")


if __name__ == "__main__":
    unittest.main()
